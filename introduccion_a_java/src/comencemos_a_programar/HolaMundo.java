package comencemos_a_programar;
/**
 * 1.2 Comencemos a programar
 * 
 * 	Un programa JAVA en una clase (class)
 * 	Contiene el metodo (o funcion) main
 * 	El metodo debe ser definido con los modificadores public,static,void y recibe un String[] como parametro
 * 	Los bloques de codigo se delimitan con {} (llaves) y las sentencias finalizan con ; (punto y coma)
 * 
 * 	En Java cada clase debe estar contenida dentro de un archivo de texto con el mismo nombre de la clase y con
 * 	extencion .java
 */
public class HolaMundo {

	public static void main(String[] args) {
		//Crear un "Hola Mundo !!"
		System.out.println("Hola Mundo !!!");

	}

}
