package com.Cap1.TratamientoDeCadenasDeCaracteres.MayusculasMinusculas;

/*
 * 1.5.2 Mayusculas y Minusculas
 * 
 */
public class Cadena {
	public static void main(String[] args) {
		String cadena = "Esta es una cadena de caracteres";
		String cadenaUpperCase = cadena.toUpperCase();
		String cadenaLoercCase = cadena.toLowerCase();
		
		System.out.println(cadena);
		System.out.println(cadenaUpperCase);
		System.out.println(cadenaLoercCase);
	}
}
