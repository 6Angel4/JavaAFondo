package com.Cap1.EstructurasDeControl.EstructurasIterativas;

/*
 * 1.3.5 Estructuras iterativas
 * While
 * while(condicion){
 * accion1;
 * }
 */

public class EstructuraWhile {
	public static void main(String[] args) {
		int numero = 20;
		while (numero >= 0) {
			System.out.println(numero);
			numero--;
		}
	}

}
